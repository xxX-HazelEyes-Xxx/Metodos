     public double redondeo(double n, int d){
          BigDecimal bigDecimal = new BigDecimal(n);
          bigDecimal = bigDecimal.setScale(d, BigDecimal.ROUND_HALF_UP);
          return bigDecimal.doubleValue();
     }

